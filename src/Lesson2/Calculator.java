package Lesson2;

public class Calculator {

   public int summary (int a, int b){
      int c = a + b;
      return c;
  }
   public long summary (long a, long b){
      long c = a + b;
      return c;
  }
  public double summary (double a, double b){
      double c = a + b;
      return c;
  }

    public int substraction (int a, int b){
        int c = a - b;
        return c;
    }
    public long substraction (long a, long b){
        long c = a - b;
        return c;
    }
    public double substraction (double a, double b){
        double c = a - b;
        return c;
    }

    public long multiplication (int a, int b){
        long c = a * b;
        return c;
    }
    public long multiplication (long a, long b){
        long c = a * b;
        return c;
    }
    public double multiplication (double a, double b){
        double c = a * b;
        return c;
    }

    public double division (int a, int b){
        double c = a / b;
        return c;
    }
    public double division (long a, long b){
        double c = a / b;
        return c;
    }
    public double division (double a, double b){
        double c = a / b;
        return c;
    }
}
